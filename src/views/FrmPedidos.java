    private void cmdAccederActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdAccederActionPerformed
        

        if (Pedido.isFecha(txtFechaPedido.getText())
                && Pedido.isFecha(txtFechaEntrega.getText()) 
                && Pedido.isNumeric(txtTotal.getText())
                && Pedido.isValidString(txtEstado.getText())) {
            
            fechaPedido = txtFechaPedido.getText();
            fechaEntrega = txtFechaEntrega.getText();
            total = txtTotal.getText();
            estado = txtEstado.getText();
            
            Pedidos.executeQuery(Conexion.getDBConexion(), String.format("insert into pedidos(id_pedido, fecha_pedido, total, fecha_entrega, estado) values(%s, '%s', %s, '%s', '%s')", id, fechaPedido, total, fechaEntrega, estado));
            JOptionPane.showMessageDialog(rootPane, "AÃ±adido exitosamente!");
            txtEstado.setText(Pedido.VACIO);
            txtFechaPedido.setText(Pedido.VACIO);
            txtFechaEntrega.setText(Pedido.VACIO);
            txtTotal.setText(Pedido.VACIO);
            
            
        }else{
            JOptionPane.showMessageDialog(rootPane, "Invalido");
        }
        
        
        
    }//GEN-LAST:event_cmdAccederActionPerformed

